    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        username = user.getText();
        email = mail.getText();
        telefone = fone.getText();
        senha = pass.getPassword().toString();
        
        try {
            Connection con = Conexao.getConnection();
            PreparedStatement stmt = con.prepareStatement("insert into cadastro (username,email,telefone,senha) VALUES(?,?,?,?)");
        stmt.setString(1, username);
        stmt.setString(2, email);
        stmt.setString(3, telefone);
        stmt.setString(4,senha);
        int rowsafecfed = stmt.executeUpdate();
         if (rowsafecfed > 0){
             
             rs.setForeground(Color.blue);
             rs.setText("Usu√°rio inserido com sucesso");
             user.setText("");
             mail.setText("");
             fone.setText("");
             pass.setText("");
             
         }stmt.close();
         con.close();
        } catch (SQLException ex) {
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

